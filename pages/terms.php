<h2>রক্তদান সিস্টেমের জন্য শর্তাবলী</h2>

<div class="terms-container">
    <h3>১. যোগ্যতা</h3>
    <p>আমাদের সিস্টেমের মাধ্যমে রক্তদান করার যোগ্য হতে হলে আপনাকে অবশ্যই:</p>
    <ul>
        <li>কমপক্ষে ১৮ বছর বয়সী হতে হবে</li>
        <li>কমপক্ষে ৫০ কেজি (১১০ পাউন্ড) ওজন হতে হবে</li>
        <li>সাধারণ স্বাস্থ্যের অবস্থা ভালো থাকতে হবে</li>
        <li> অবশ্যই ডাক্তারের পরামর্শের সাথে সম্মত হতে হবে</li>
    </ul>

    <h3>২. নিবন্ধন এবং ব্যক্তিগত তথ্য</h3>
    <p>আমাদের প্ল্যাটফর্মে নিবন্ধন করে, আপনি সম্মত হচ্ছেন:</p>
    <ul>
        <li>নিজের সম্পর্কে সঠিক এবং সম্পূর্ণ তথ্য প্রদান করুন</li>
        <li>আপনার তথ্য সঠিক, হালনাগাদ এবং সম্পূর্ণ রাখতে আপডেট করুন</li>
        <li>আপনার অ্যাকাউন্টের প্রমাণপত্র আমরা কারো সাথে শেয়ার করব না</li>
    </ul>

    <h3>৩. গোপনীয়তা এবং তথ্য সুরক্ষা</h3>
    <p>আমরা আপনার গোপনীয়তা রক্ষায় প্রতিশ্রুতিবদ্ধ। আপনার ব্যক্তিগত তথ্য:</p>
    <ul>
        <li>শুধুমাত্র রক্তদান এবং সংশ্লিষ্ট চিকিৎসা সেবার উদ্দেশ্যে ব্যবহৃত হবে</li>
        <li>আপনার স্পষ্ট সম্মতি ছাড়া তৃতীয় পক্ষের সাথে শেয়ার করা হবে না</li>
        <li>প্রাসঙ্গিক তথ্য সুরক্ষা আইন অনুযায়ী নিরাপদে সংরক্ষণ করা হবে</li>
    </ul>

    <h3>৪. রক্তদান প্রক্রিয়া</h3>
    <p>রক্তদানে সম্মত হওয়ার সময় আপনি স্বীকার করছেন যে:</p>
    <ul>
        <li>প্রতিটি দানের আগে আপনি একটি স্বাস্থ্য পরীক্ষার মধ্য দিয়ে যাবেন</li>
        <li>দান প্রক্রিয়ার আগে বা চলাকালীন যে কোনো সময় সম্মতি প্রত্যাহার করার অধিকার আপনার রয়েছে</li>
        <li>রক্তদানের সাথে সামান্য ঝুঁকি জড়িত থাকতে পারে, যা আপনাকে আগে খেকে জেনে নিতে হবে</li>
    </ul>

    <h3>৫. রক্ত গ্রহীতাদের দায়িত্ব</h3>
    <p>যদি আপনি আমাদের সিস্টেমের মাধ্যমে রক্তের অনুরোধ করেন, তাহলে আপনি সম্মত হচ্ছেন:</p>
    <ul>
        <li>আপনার চিকিৎসা প্রয়োজনীয়তা সম্পর্কে সঠিক তথ্য প্রদান করতে</li>
        <li>প্রাপ্ত রক্ত শুধুমাত্র উদ্দিষ্ট চিকিৎসা উদ্দেশ্যে ব্যবহার করতে</li>
        <li>আমাদের সিস্টেম সেবা নিতে আমাদের কোন প্রকার টাকা প্রদান করতে হয় না ।</li>
    </ul>

    <h3>৬. দায়বদ্ধতার সীমাবদ্ধতা</h3>
    <p>যদিও আমরা একটি নিরাপদ এবং দক্ষ রক্তদান সিস্টেম প্রদান করার চেষ্টা করি, আমরা নিম্নলিখিত বিষয়গুলির জন্য দায়ী থাকব না:</p>
    <ul>
        <li>দান বা ট্রান্সফিউশন প্রক্রিয়া থেকে উদ্ভূত কোনো স্বাস্থ্য জটিলতা</li>
        <li>ব্যক্তিগত দাতা বা গ্রহীতাদের কার্যকলাপ</li>
        <li>রক্ষণাবেক্ষণ বা অপ্রত্যাশিত পরিস্থিতির কারণে সিস্টেমের অস্থায়ী অনুপলব্ধতা</li>
    </ul>

    <h3>৭. শর্তাবলীর পরিবর্তন</h3>
    <p>আমরা যে কোনো সময় এই শর্তাবলী পরিবর্তন করার অধিকার সংরক্ষণ করি। কোনো উল্লেখযোগ্য পরিবর্তন হলে ব্যবহারকারীদের অবহিত করা হবে।</p>

    <h3>৮. যোগাযোগের তথ্য</h3>
    <p>এই শর্তাবলী সম্পর্কে আপনার কোনো প্রশ্ন থাকলে, অনুগ্রহ করে আমাদের সাথে যোগাযোগ করুন:</p>
